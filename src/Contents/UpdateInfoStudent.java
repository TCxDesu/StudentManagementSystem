    private void txtNextMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_txtNextMouseClicked
        if (p < 2) {
            p += 1;
        }
        switch (p) {
            case 1:
                pnlOne.setVisible(false);
                pnlTwo.setVisible(true);
                break;
            case 2:
                pnlTwo.setVisible(false);
                pnlThree.setVisible(true);
                break;
            default:
                break;
        }
        System.out.println(p);
    }//GEN-LAST:event_txtNextMouseClicked

    private void txtPrevMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_txtPrevMouseClicked
        if (p > 0) {
            p--;
        }
        switch (p) {
            case 0:
                pnlOne.setVisible(true);
                pnlTwo.setVisible(false);
                break;
            case 1:
                pnlTwo.setVisible(true);
                pnlThree.setVisible(false);
                break;
            default:
                break;
        }
        System.out.println(p);
    }//GEN-LAST:event_txtPrevMouseClicked

